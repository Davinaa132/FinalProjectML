if st.button("ğŸ” Deteksi"):
    if judul and isi:
        full_text = judul + " " + isi
        X_input = vectorizer.transform([full_text])
        prediction = model.predict(X_input)[0]
        proba = model.predict_proba(X_input)[0][prediction]

        if prediction == 1:
            st.error(f"ğŸš¨ Deteksi: **HOAKS** (Probabilitas: {proba:.2f})")
